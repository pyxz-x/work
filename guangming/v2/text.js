// 计算贝塞尔曲线的轨迹
function make_trace(x1, y1, x2, y2) {
    // 计算步数：根据距离自动决定，最小 3 步
    var dx = x2 - x1, dy = y2 - y1;
    var dist = Math.sqrt(dx * dx + dy * dy);
    var steps = Math.max(3, Math.floor(dist / 10));

    // 用确定性的控制点（相对位移，不依赖随机）
    // 控制点靠近起点与终点，但带有横向偏移以制造弧度
    var cx1 = x1 + dx * 0.25 - dy * 0.2;
    var cy1 = y1 + dy * 0.25 + dx * 0.2;
    var cx2 = x1 + dx * 0.75 + dy * 0.2;
    var cy2 = y1 + dy * 0.75 - dx * 0.2;

    // 使用 easeInOut 时间函数调整速度（平滑加速与减速）
    function easeInOut(t) {
        return (1 - Math.cos(t * Math.PI)) / 2;
    }

    // 计算三次贝塞尔（四点）曲线，使用伯恩斯坦多项式
    function bernstein(i, n, t) {
        function comb(a, b) {
            var res = 1;
            for (var k = 1; k <= b; k++) {
                res = res * (a - (b - k)) / k;
            }
            return Math.round(res);
        }
        return comb(n, i) * Math.pow(t, i) * Math.pow(1 - t, n - i);
    }

    var pts = [
        [x1, y1],
        [cx1, cy1],
        [cx2, cy2],
        [x2, y2]
    ];

    var traces = [];
    for (var step = 0; step <= steps; step++) {
        var t = step / steps;
        var easedT = easeInOut(t); // 应用 ease-in-out 曲线

        var x = 0, y = 0;
        for (var i = 0; i < 4; i++) {
            var B = bernstein(i, 3, easedT); // 用加速函数调整 t
            x += pts[i][0] * B;
            y += pts[i][1] * B;
        }
        traces.push([Math.round(x), Math.round(y)]);
    }
    return traces;
}

